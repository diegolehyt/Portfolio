$(window).on('resize', function() {
    var win = $(this);
    if (win.width() >= 554) {

      $('#Nright').removeClass('justify-content-end');
      $('#Nright').addClass('justify-content-center');
  
    } 
    else {
      $('#Nright').addClass('justify-content-end');
    }
});
